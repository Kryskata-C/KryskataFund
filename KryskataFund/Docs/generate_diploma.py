def add_page_break():
    doc.add_page_break()

def add_paragraph(text, bold=False, italic=False, alignment=None, font_size=None, space_after=None, first_line_indent=None):
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.bold = bold
    run.italic = italic
    run.font.name = 'Times New Roman'
    if font_size:
        run.font.size = Pt(font_size)
    if alignment:
        p.alignment = alignment
    if space_after is not None:
        p.paragraph_format.space_after = Pt(space_after)
    if first_line_indent:
        p.paragraph_format.first_line_indent = Cm(first_line_indent)
    return p

def add_body_text(text, first_line_indent=1.25):
    """Standard body text with first-line indent."""
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Cm(first_line_indent)
    p.paragraph_format.line_spacing = 1.5
    p.paragraph_format.space_after = Pt(8)
    run = p.add_run(text)
    run.font.name = 'Times New Roman'
    run.font.size = Pt(12)
    return p

def add_bullet(text, level=0):
    p = doc.add_paragraph(style='List Bullet')
    p.clear()
    run = p.add_run(text)
    run.font.name = 'Times New Roman'
    run.font.size = Pt(12)
    p.paragraph_format.line_spacing = 1.5
    p.paragraph_format.space_after = Pt(6)
    if level > 0:
        p.paragraph_format.left_indent = Cm(1.25 * (level + 1))
    return p

def add_numbered_item(text, level=0):
    p = doc.add_paragraph(style='List Number')
    p.clear()
    run = p.add_run(text)
    run.font.name = 'Times New Roman'
    run.font.size = Pt(12)
    return p

